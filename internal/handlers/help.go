package handlers

import (
	"go.uber.org/zap"
	tele "gopkg.in/telebot.v4"
)

func HelpHandler(b *tele.Bot, log *zap.Logger) func(c tele.Context) error {
	return func(c tele.Context) error {
		msg := `ℹ️ <b>DailyDoseBot — помощник для учёта и напоминаний о приёме добавок</b>

<b>Что умеет бот:</b>
• Напоминать о приёме добавок в нужное время (можно несколько раз в день)
• Помогать отмечать приём одним нажатием
• Вести историю и показывать прогресс за день и неделю
• Автоматически считать процент выполнения плана
• Показывать подробную статистику за неделю (каждый понедельник)
• Помогать не забыть завершить курс или сдать анализы

<b>Основные команды:</b>
/add — добавить новую добавку
/list — список всех добавок
/log — отметить приём вручную
/status — статус и прогресс за сегодня
/help — показать это сообщение

<b>Советы:</b>
- Используй /log или кнопки в напоминаниях для быстрого трекинга
- Следи за прогрессом и не пропускай приёмы!
- Каждый понедельник бот пришлёт недельную статистику

Если есть вопросы или предложения — просто напиши мне! ❤️`
		return c.Send(msg, &tele.SendOptions{ParseMode: tele.ModeHTML})
	}
}
